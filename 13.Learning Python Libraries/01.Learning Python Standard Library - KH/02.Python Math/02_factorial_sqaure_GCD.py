# Math Module Part 2
import math

# Factorial & Square Root
print(math.factorial(3))
print(math.sqrt(64))

# Greatest Common Denominator GCD
print(math.gcd(52, 8))
print(math.gcd(8, 52))

# Degrees and Radians
print(math.radians(360)) # how many radians are in 360 degrees
print(math.pi * 2) # as 2pi is same as 360 dregree

# reversely, we can get degree from radians
print(math.degrees(math.pi * 2))