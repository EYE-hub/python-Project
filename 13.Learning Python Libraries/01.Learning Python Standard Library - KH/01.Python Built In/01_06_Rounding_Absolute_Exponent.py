# Python Rounding, Absolute Value, and Exponents

# round()
myGPA = 3.6
print(round(myGPA))

salt = 1.4
print(round(salt))

apple = -1.2
print(round(apple))

google = -1.6
print(round(google))


# abs()
print(abs(myGPA))
print(abs(google))

lengthOfRootInGround = -2.5
print(abs(lengthOfRootInGround))

# pow()
print(pow(3,2))

chanceOfTails = 0.5
inARowTails = 3
print(pow(chanceOfTails, inARowTails))